
# Estruturas de repetição - loops
# while 
# for
# do while*

a = 0 
while a <= 5:  # while = enquanto...
    print(a <= 5)
    print(a)
    
    a = a + 1

print('Resultado final de a:', a)   # resposta final com valor maior que 5, por tanto fora do loop.
print(a <= 5)                       #Resposta falsa por que nao foi possivel entrar no loop
